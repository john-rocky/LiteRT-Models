object Colormap {

    fun applyInferno(grayscale: Bitmap): Bitmap {
        val w = grayscale.width
        val h = grayscale.height
        val pixels = IntArray(w * h)
        grayscale.getPixels(pixels, 0, w, 0, 0, w, h)

        for (i in pixels.indices) {
            val gray = pixels[i] and 0xFF
            pixels[i] = INFERNO[gray]
        }

        val colored = Bitmap.createBitmap(w, h, Bitmap.Config.ARGB_8888)
        colored.setPixels(pixels, 0, w, 0, 0, w, h)
        return colored
    }

